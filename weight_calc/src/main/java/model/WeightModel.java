package model;

public class WeightModel {

    public double convertGramsIntoKilos(double in){

        return in*0.001;

    }

    public double convertGramsIntoDecagram(double in){

        return in*0.1;

    }

    public double convertGramsIntoOunce(double in){

        return in*0.0352734;

    }

    public double convertGramsIntoPound(double in){

        return in*0.00220459;

    }

}
